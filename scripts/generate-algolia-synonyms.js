#!/usr/bin/env node

/*
  Generate Algolia synonym groups for every operation in the page-model registry.

  For each `operationId` in src/data/generatedFastnearPageModels.json, emit a
  synonym group that treats the snake_case, hyphen-case, and space-separated
  forms as equivalent. A user who types `view_account`, `view-account`, or
  `view account` should get the same results.

  Generated groups use objectIDs prefixed with `fastnear-op-`. The generator
  rewrites every `fastnear-op-*` entry on each run and preserves any other
  hand-crafted conceptual entries (auth, bearer-token, api-key, etc.) in place.

  Operation IDs without a `_` separator (e.g. `status`, `health`) collapse to
  a single variant and are skipped — Algolia rejects synonym groups with
  fewer than two members.

  Run:
      yarn generate:algolia-synonyms
*/

const fs = require("node:fs");
const path = require("node:path");

const ROOT = path.resolve(__dirname, "..");
const PAGE_MODELS_PATH = path.join(ROOT, "src/data/generatedFastnearPageModels.json");
const SYNONYMS_PATH = path.join(ROOT, "algolia/synonyms.json");
const AUTOGEN_PREFIX = "fastnear-op-";

function collectOperationIds(pageModels) {
  const ids = new Set();

  const walk = (node) => {
    if (!node || typeof node !== "object") {
      return;
    }

    if (Array.isArray(node)) {
      node.forEach(walk);
      return;
    }

    if (typeof node.operationId === "string" && node.operationId.length > 0) {
      ids.add(node.operationId);
    }

    for (const value of Object.values(node)) {
      walk(value);
    }
  };

  walk(pageModels);
  return [...ids].sort();
}

function variantsFor(operationId) {
  const lower = operationId.toLowerCase();
  const snake = lower;
  const hyphen = lower.replace(/_/g, "-");
  const spaced = lower.replace(/_/g, " ");

  const variants = new Set([snake, hyphen, spaced]);

  if (operationId !== lower) {
    variants.add(operationId);
  }

  return [...variants];
}

function buildAutogenEntries(operationIds) {
  return operationIds
    .map((operationId) => ({
      objectID: `${AUTOGEN_PREFIX}${operationId}`,
      type: "synonym",
      synonyms: variantsFor(operationId),
    }))
    .filter((entry) => entry.synonyms.length >= 2);
}

function main() {
  const pageModels = JSON.parse(fs.readFileSync(PAGE_MODELS_PATH, "utf8"));
  const operationIds = collectOperationIds(pageModels);

  const existing = fs.existsSync(SYNONYMS_PATH)
    ? JSON.parse(fs.readFileSync(SYNONYMS_PATH, "utf8"))
    : [];

  const preserved = existing.filter(
    (entry) => typeof entry.objectID === "string" && !entry.objectID.startsWith(AUTOGEN_PREFIX)
  );

  const autogen = buildAutogenEntries(operationIds);
  const next = [...preserved, ...autogen];

  fs.writeFileSync(SYNONYMS_PATH, `${JSON.stringify(next, null, 2)}\n`, "utf8");

  console.log(
    `Wrote ${SYNONYMS_PATH} with ${preserved.length} preserved + ${autogen.length} autogenerated synonym groups.`
  );
}

main();
