export function getFastnearAuthSummaryText(pageModel, auth, uiText = getFastnearOperationUiText()) {
  if (!auth.apiKey) {
    return uiText.noAuthDetected;
  }

  if (pageModel.interaction.authTransport === 'bearer') {
    return auth.apiKeySource === 'url'
      ? translate({
          id: 'fastnear.operationUi.authSummary.urlBearer',
          message: 'API key from URL via Bearer auth',
        })
      : translate({
          id: 'fastnear.operationUi.authSummary.savedBearer',
          message: 'saved browser API key via Bearer auth',
        });
  }

  return auth.apiKeySource === 'url'
    ? translate({
        id: 'fastnear.operationUi.authSummary.urlQuery',
        message: 'API key from URL via query param',
      })
    : translate({
        id: 'fastnear.operationUi.authSummary.savedQuery',
        message: 'saved browser API key via query param',
      });
}

export function getFastnearApiKeyStatusText(auth, uiText = getFastnearOperationUiText()) {
  if (auth.apiKeySource === 'url') {
    return uiText.fromUrlOverride;
  }

  if (auth.storedApiKey) {
    return uiText.savedBrowserKey;
  }

  return uiText.noSavedKey;
}

export function getFastnearFieldLocationLabel(field, uiText = getFastnearOperationUiText()) {
  if (field.location === 'path') {
    return uiText.pathParameter;
  }

  if (field.location === 'query') {
    return uiText.queryParameter;
  }

  if (field.location === 'body') {
    return uiText.requestBodyField;
  }

  return undefined;
}

