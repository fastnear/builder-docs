export function getFastnearRequestSummary(pageModel, httpHasBody) {
  if (pageModel.route.transport === 'json-rpc') {
    return translate(
      {
        id: 'fastnear.operationUi.requestSummary.jsonRpc',
        message: 'This operation accepts a JSON-RPC body over {method} to {path}.',
      },
      {
        method: pageModel.route.method,
        path: pageModel.route.path,
      }
    );
  }

  if (httpHasBody) {
    return translate(
      {
        id: 'fastnear.operationUi.requestSummary.httpWithBody',
        message:
          'This operation performs {method} {path} with an {mediaType} request body.',
      },
      {
        mediaType: pageModel.request.mediaType || 'HTTP',
        method: pageModel.route.method,
        path: pageModel.route.path,
      }
    );
  }

  return translate(
    {
      id: 'fastnear.operationUi.requestSummary.http',
      message: 'This operation performs {method} {path}.',
    },
    {
      method: pageModel.route.method,
      path: pageModel.route.path,
    }
  );
}
